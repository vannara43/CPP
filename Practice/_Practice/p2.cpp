// Practice 2: A Program for Use of Variables

#include <iostream>
#include <string>
using namespace std;
int main()
{
    int x, y, z;
    string hello = "Peter Paul";
    x = 20;
    y = 9;
    z = x + y;
    cout << "Use of Variables in C++" << endl;
    cout << "The result of x + y = " << z << endl;
    cout << "My name is " << hello << endl;
    return 0;
}
