using namespace std;

int main()
{
    int x = 67;
    cout << "if else statement \n"
         << endl;
    if (x == 69)
    {
        cout << "This is a correct if statement " << endl;
    }
    else
    {
        cout << "Incorrect password" << endl;
    }
    return 0;
}
